public class Operacoes {


    public static void main(String[] args) {
        int x = 5;
        int y = 2;

        System.out.println("Soma = " + (x + y ));
        System.out.println("Subtração = " + (x - y) );
        System.out.println("Multiplicação = " + x * y);
        System.out.println("Divisão = " + (float) y / x);
    }
}
